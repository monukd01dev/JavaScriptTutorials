// Boolean & comparison operator

// booleans
// true, false

let num1 = 2;
let num2 = "2";

console.log('== vs ===');

console.log( num1==num2); //it will not check the datatypes of operands.
console.log(num1===num2); //it will check the datatypes of operands.

// != vs !==
console.log('!= vs !==');

console.log( num1!=num2);
console.log( num1!==num2);
