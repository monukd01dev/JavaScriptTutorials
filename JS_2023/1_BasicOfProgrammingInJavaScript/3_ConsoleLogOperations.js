var value = 2;

// basic arithmetic operations

console.log(value + 2);

console.log(value - 2);

console.log(value / 2);

console.log(value % 2);

console.log(value * 2);

// exponential using **

console.log(value ** 2); //2^2 = 4
console.log(value ** 3); //2^3 = 8

// root
console.log(value ** 0.5);
